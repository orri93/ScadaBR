package br.org.scadabr.view;

/**
 *
 * @author aploese
 */
public interface FormatPatternHolder {
    
    /**
     * a pattern for {@link java.text.MessageFormat} which formats only the value itself
     * @return the pattern
     * @see java.text.MessageFormat
     * 
     */
    String getValueMessagePattern();
    
    /**
     * a pattern for {@link java.text.MessageFormat} which formats the value with a suffix
     * @return the pattern
     * @see java.text.MessageFormat
     * 
     */
    String getMessagePattern();
    
    
    String getSuffix();
    
}
